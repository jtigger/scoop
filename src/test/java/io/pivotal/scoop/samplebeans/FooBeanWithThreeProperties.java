package io.pivotal.scoop.samplebeans;

public class FooBeanWithThreeProperties {
	private String baz;
	private String ree;
	private String qux;

	public String getBaz() {
		return baz;
	}

	public void setBaz(String baz) {
		this.baz = baz;
	}

	public String getRee() {
		return ree;
	}

	public void setRee(String ree) {
		this.ree = ree;
	}

	public String getQux() {
		return qux;
	}

	public void setQux(String qux) {
		this.qux = qux;
	}
}
