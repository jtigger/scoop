package io.pivotal.scoop.samplebeans;

public class FooBeanWithTwoProperties {
	private String baz;
	private String ree;

	public String getBaz() {
		return baz;
	}

	public void setBaz(String baz) {
		this.baz = baz;
	}

	public String getRee() {
		return ree;
	}

	public void setRee(String ree) {
		this.ree = ree;
	}
}
